package com.wiprof.wirelessprofiler;

public class AttributeEntry {
    public String name;
    public String value;
    public String infoText;

    public AttributeEntry(String in_name, String in_value, String in_infoText) {
        name = in_name;
        value = in_value;
        infoText = in_infoText;
    }
}
